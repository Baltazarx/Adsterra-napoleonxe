export function getMovieById(id) {
  return movies.find((m) => m.id === parseInt(id));
}

export function getMoviesByType(type) {
  return movies.filter((m) => m.type === type);
}

export function getFeaturedMovies() {
  return movies.filter((m) => m.featured);
}

export function getMoviesByGenre(genre) {
  return movies.filter((m) => m.genre.includes(genre));
}

export function searchMovies(query) {
  const q = query.toLowerCase();
  return movies.filter(
    (m) =>
      m.title.toLowerCase().includes(q) ||
      m.genre.some((g) => g.toLowerCase().includes(q)) ||
      m.description.toLowerCase().includes(q)
  );
}
